    0xdfffe,
    0xdffff,
    0xefffe,
    0xeffff,
    0xffffe,
    0xfffff,
    0x10fffe,
    0x10ffff
]);

function _replace_charref(&$s)
{
    global $_invalid_charrefs, $_invalid_codepoints, $_html5;

    $s = $s[1];
    if ($s[0] == "#") {
        // numeric charref
        if (std\contains("xX", $s[1]))
            $num = intval(rtrim(std\substring($s, 2), ";"), 16);
        else
            $num = (int)rtrim(std\substring($s, 1), ";");

        if (isset($_invalid_charrefs[$num]))
            return std\CString::unicode2utf($_invalid_charrefs[$num]);
        if (0xD800 <= $num && $num <= 0xDFFF || $num > 0x10FFFF)
            return std\CString::unicode2utf(0xFFFD);
        if (isset($_invalid_codepoints[$num]))
            return "";

        return chr($num);
    } else {
        // named charref
        if (isset($_html5, $s))
            return std\CString::unicode2utf($_html5[$s]);
        // find the longest matching name (as defined by the standard)
        foreach (range(strlen($s) - 1, 1, - 1) as $x) {
            $former = std\substring($s, 0, $x);
            if (isset($_html5[$former]))
                return std\CString::unicode2utf($_html5[$former]).std\substring($s, $x);
        }

        return "&" + $s;
    }
}

function unescape($s)
{
    if (std\contains($s, "&")) {
        return preg_replace_callback("/&(#[0-9]+;?|#[xX][0-9a-fA-F]+;?|[^\t\n\f <&#;]{1,32};?)/u", "_replace_charref", $s);
    } else
        return $s;
}

