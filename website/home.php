	<h1>What is sagemath.tech?</h1>
	<p>
		<a href="../axiom"> sagemath.tech</a> is a
		website for symbolic semi-mechanized axiomatized theorem-proving
		system, based on open-source symbolic computation project of sympy and
		<a href="http://maxima.sourceforge.net"> Maxima </a> , as well as on
		commercial algebraic system <a
			href="https://reference.wolfram.com/language/index.html.en?source=footer">
			Mathematica</a>.
	</p>
	<br>
	<p>
		In 2008, the writer embarked on the development of this axiomatized
		mathematical analysis system. The project is being developed mainly in
		the writer's spare time. At that time, the writer of this tool was
		still an undergraduate student. It was originally written in C++,
		based on a deutch project <a href="https://www.ginac.de/"> ginac</a>
		which is a C++ library for symbolic mathematical calculations. The
		axiom proving system was build on top of it.
	</p>
	<br>
	<p>
		In 2016, the writer discovered another open-source symbolic
		computation project, namely <a
			href="https://www.sympy.org/en/index.html"> sympy</a>, and another
		C++ project <a href="https://github.com/symengine/symengine.git">
			symengine</a> which is subset of sympy, as well as <a
			href="http://maxima.sourceforge.net"> Maxima </a> which is a
		Common-Lisp project. As Python is an more efficient language in
		developing (but not in executing) than C++, and a more popular /
		universal language than Common-Lisp, the writer mainly focused on
		developing with Python. Since then, this project was rewritten as /
		translated into a Python project.<br>
	</p>
	<br>
	<p>
		In 2018, the writer established a website, namely <a
			href="http://www.sagemath.tech/sympy"> sagemath.tech</a> for the
		purpose of sharing this axiomatized reasoning system. The writer also
		hopes that with the aid of open-source community, a large data-set can
		be established by volunteer engineers, so that further development of
		advanced analysis tool based on machine-learning methods can become
		possible.
	</p>
	<br>

	<p>
		Mathematics Mechanization is the goal of the institute: <a
			href="http://www.mmrc.iss.ac.cn/"> Key Laboratory of Mathematics
			Mechanization, CAS</a>. So far, its research result is not open to
		general public. As of this writing, in world wide, Mathematics
		Mechanization is still a challenging problem. The final goal of
		sagemath.tech is to develop fully-mechanized axiomatized mathematical
		analysis system, which is open to the general public. So far, it has
		collected roughly 800 mathematical theorems, which are proved by this
		semi-mechanized mathematical proving system. The ultimate goal will be
		realized through the following phases:
	</p>
	<ul>
		<li>(1) development of rule-bases semi-mechanized axiomatized
			reasoning system which is done so far, but there are known bugs and
			unknown bugs which needs to be overcome;</li>
		<li>(2) collection of over 100,000 mathematical theorems which can be
			proved by semi-mechanized reasoning system, which is a time-consuming
			task, roughly taking 5-10 years effort to finish;</li>
		<li>(3) develop a supervised deep-learning / neural-network model
			which can capture the features of mathematical proving process, and
			train this model on the basis of data collected in previous step;</li>
		<li>(4) devise a semi-supervised / unsupervised model which is able to
			detect unknown theorems that can be proved by applying the existing
			known theorems, and try to prove with a brute-force method.</li>
		<li>(5) the complete mastery of abstract mathematical language in
			computer programming language can be achieved on basis of previous
			step.</li>
	</ul>
